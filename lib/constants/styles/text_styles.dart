import 'package:flutter/material.dart';

boldText() {
  return const TextStyle(fontSize: 24, fontFamily: 'semi-bold');
}

simpleBoldText() {
  return const TextStyle(fontFamily: 'semi-bold', fontSize: 16);
}

headText() {
  return const TextStyle(
      fontSize: 18.0, fontFamily: 'semi-bold', color: Colors.black);
}

simpleText() {
  return const TextStyle(fontFamily: 'regular', fontSize: 14);
}
